def encode_sentence(tokens, max_len):
    ids = [word2idx[w] for w in tokens]
    mask = [1] * len(ids)
    while len(ids) < max_len:
        ids.append(word2idx["<pad>"])
        mask.append(0)
    return ids, mask

encoded = [encode_sentence(sent, max_len) for sent in tokenized]
input_ids = torch.tensor([x[0] for x in encoded], dtype=torch.long)   # [B, T]
